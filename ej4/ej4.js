//Para declarar funciones se puede usar tanto "var" como "let".

//Declaramos las variables con "let" para almacenar numeros.
let num1 = 10;
let num2 = 5;

//Declaramos variables para las operaciones.
let suma;
let resta;
let division;

// Realizamos las operaciones
suma = num1 + num2;         // Suma
resta = num1 - num2;        // Resta
division = num1 / num2;     // División

console.log("Suma: " + suma);           // Suma: 15
console.log("Resta: " + resta);         // Resta: 5
console.log("División: " + division);   // División: 2
